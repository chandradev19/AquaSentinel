export const generateHealthAssessment = (analytics) => {
  const doc = new jsPDF();
  addBranding(doc);
  
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(18);
  doc.text('National Health Assessment', 14, 40);
  
  doc.setFontSize(12);
  doc.text(`Active Outbreaks: ${analytics.alertStats?.active || 0}`, 14, 55);
  doc.text(`Safe Water Sources: ${analytics.waterQuality?.safe || 0}`, 14, 65);
  doc.text(`Contaminated Sources: ${analytics.waterQuality?.unsafe || 0}`, 14, 75);

  doc.setFontSize(14);
  doc.text('District Risk Rankings', 14, 95);

  const districtData = (analytics.districtRanking || []).map(d => [
    d.district,
    d.activeCases,
    d.riskScore.toFixed(1) + '%'
  ]);

  doc.autoTable({
    startY: 100,
    head: [['District', 'Active Cases', 'Average Risk Score']],
    body: districtData,
    theme: 'grid',
    headStyles: { fillColor: [16, 185, 129] }
  });

  addFooter(doc);
  doc.save(`Health_Assessment_${new Date().getTime()}.pdf`);
};

export const generateRiskAnalysis = (villages) => {
  const doc = new jsPDF();
  addBranding(doc);
  
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(18);
  doc.text('National Risk Analysis Matrix', 14, 40);
  
  doc.setFontSize(12);
  doc.text('High Risk Predictive Index:', 14, 55);

  const riskData = villages.map(v => [
    v.name,
    v.district,
    v.riskScore?.toFixed(1) + '%',
    v.waterQualityStatus,
    v.activeCases
  ]).sort((a, b) => parseFloat(b[2]) - parseFloat(a[2])); // sort by risk

  doc.autoTable({
    startY: 65,
    head: [['Village', 'District', 'Risk Score', 'Water Quality', 'Active Cases']],
    body: riskData,
    theme: 'grid',
    headStyles: { fillColor: [139, 92, 246] } // brand-accent/purple
  });

  addFooter(doc);
  doc.save(`Risk_Analysis_${new Date().getTime()}.pdf`);
};
